#include "libft.h"

char	*ft_strchr(const char *str, int c)
/*
	This function finds the first occurrence of a character 'c'
	Searches left to right + special case '\0'
	Returns pointer to the character
*/
{
	while (*str)
	{
		if (*str == (char)c)
			return ((char *)str);
		str++;
	}
	if (c == '\0')
		return ((char *)str);
	return (NULL);
}
