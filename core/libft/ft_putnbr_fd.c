#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
/*
    This function outputs the integer ’n’ to the specified file
    descriptor 'fd' (edge cases are solved before the first recursion)
*/
{
	long	num;

	num = n;
	if (fd < 0)
		return ;
	if (num < 0)
	{
		ft_putchar_fd('-', fd);
		num = -num;
	}
	if (num >= 10)
		ft_putnbr_fd(num / 10, fd);
	ft_putchar_fd((num % 10) + '0', fd);
}
